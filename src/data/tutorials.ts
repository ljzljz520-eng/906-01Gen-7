export const getTutorials = (): Tutorial[] => tutorials;

export const getTutorialById = (id: string): Tutorial | undefined => {
  return tutorials.find(t => t.id === id);
};

export const getTutorialsByCategory = (category: Category): Tutorial[] => {
  return tutorials.filter(t => t.category === category);
};

export const getTutorialsByDifficulty = (difficulty: Difficulty): Tutorial[] => {
  return tutorials.filter(t => t.difficulty === difficulty);
};

export const getTutorialsByAuthor = (authorId: string): Tutorial[] => {
  return tutorials.filter(t => t.authorId === authorId);
};

export const searchTutorials = (keyword: string): Tutorial[] => {
  const lowerKeyword = keyword.toLowerCase();
  return tutorials.filter(t =>
    t.title.toLowerCase().includes(lowerKeyword) ||
    t.description.toLowerCase().includes(lowerKeyword)
  );
};
